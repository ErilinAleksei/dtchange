#!/bin/bash

test_file="test.txt"
programm="../dtchange"

function test_modificed(){	
	echo "      Правильные данные"
	echo "-------------------------------------"
	echo ""
	
	$programm -m $test_file -t 20:30:40
	echo "Изменения времени модификации"
	echo "-------------------------------------"
	echo "Входные данные: время 20:30:40"
	stat $test_file | grep -w "20:30:40"
	echo "-------------------------------------"
	echo ""
	
	$programm -m $test_file -d 03:12:2015
	echo "Изменения даты модификации"
	echo "-------------------------------------"
	echo "Входные данные: дата 03:12:2015"
	stat $test_file | grep -w "2015-12-03"
	echo "-------------------------------------"
	echo ""
	
	$programm -m $test_file -t 20-30-40 -d 03.12.2015
	echo "Изменения даты и времени модификации"
	echo "-------------------------------------"
	echo "Входные данные: время 20-30-40, дата 03.12.2015"
	stat $test_file | grep -w "2015-12-03 20:30:40"
	echo "-------------------------------------"
	echo ""
	
	$programm -m $test_file
	echo "Изменения даты и времени модификации"
	echo "-------------------------------------"
	echo "Входные данные: текущее вермя"
	stat $test_file | grep -w "Модифицирован:"
	echo "-------------------------------------"
	echo ""
	
	echo "      [Неправильные данные]"
	echo ""
	
	echo "Изменения даты модификации"
	echo "-------------------------------------"
	echo "Входные данные: время 20:03 2015"
	$($programm -m $test_file -d 20:03 2015)
	echo "-------------------------------------"
	echo ""
	
	echo "Изменения времени модификации"
	echo "-------------------------------------"
	echo "Входные данные: время 20 30 40"
	$($programm -m $test_file -t 20 30 40)
	echo "-------------------------------------"
	echo ""
	
	echo "Неверный аргумент"
	echo "-------------------------------------"
	echo "Входные данные: параметр -s, время 20-15-45"
	$($programm -a $test_file -s 20-15-45)
	echo "-------------------------------------"
	echo ""
}

test_modificed
